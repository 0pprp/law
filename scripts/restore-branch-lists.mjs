function resolveBranchId(branches, keys) {
  for (const name of keys) {
    const hit = branches.find(b => b.name === name && b.is_active !== false)
    if (hit) return hit.id
  }
  return null
}

const { data: branches, error: bErr } = await sb.from('branches').select('id, name, is_active')
if (bErr) throw bErr

let inserted = 0
let skipped = 0
for (const [keys, listName] of SEED) {
  const branchId = resolveBranchId(branches ?? [], keys)
  if (!branchId) {
    skipped++
    continue
  }
  const { error } = await sb.from('branch_lists').upsert(
    { branch_id: branchId, name: listName },
    { onConflict: 'branch_id,name', ignoreDuplicates: true },
  )
  if (error && !error.message.includes('duplicate')) {
    console.error('insert failed', listName, error.message)
    process.exit(1)
  }
  inserted++
}

const { count } = await sb.from('branch_lists').select('*', { count: 'exact', head: true })
console.log(`branch_lists restored: ${count} rows (${inserted} processed, ${skipped} skipped — branch not found)`)
